% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
path=uigetimagefile();
img= imread(path);
imshow(img, [], 'Parent', handles.axes1);
image=img;
[w,h] = size(image);
new_image=fft2(image);
y=fft2(image);
for width=1:w
    for hight=1:h
        r=real(new_image(width,hight));
        im=imag(new_image(width,hight));
        res=sqrt((r*r)+(im*im));
        new_image(width,hight)=res;
    end
end
new_image=log2(new_image+1);
new_image=mat2gray(new_image);
for width=1:w
    for hight=1:h
        r=real(new_image(width,hight));
        im=imag(new_image(width,hight));
        res=sqrt((r*r)+(im*im));
        new_image(width,hight)=res;
    end
end
new_image=log2(new_image+1);
new_image=mat2gray(new_image);
imshow(new_image, [], 'Parent', handles.axes2);
new_image1=fftshift(new_image);
imshow(new_image1, [], 'Parent', handles.axes3);
y=ifft2(y);
y=uint8(y);
imshow(y, [], 'Parent', handles.axes4);



